//7.Create a program to swap two numbers.
public class Swap {
    public static void main(String[] args) {
        int a=20;
        int b=40;
        System.out.println("The Value of a is "+a);
        System.out.println("The Value of b is "+b);

        int c=a;
        a=b;
        b=c;

        System.out.println("The Value of a is "+a);
        System.out.println("The Value of b is "+b);


    }
}